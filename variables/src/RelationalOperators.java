public class RelationalOperators {
    public static void main (String[] args){
        // 2. arithmetic operators -> are used to compare two operands and get a boolean value.
        // Output after using relational operators is always boolean because they are more of
        //comparison type.

        // == (equal to) : this operator compares both the operands and returns true if both
        // operators are equal to each else it will return false
        // != (not equal to) : this operator compares both the operands and returns true if both
        // operators are equal to each else it will return false
        // < (less than) : compares both operands and returns true if operand on the left hand
        // side is less than the value on the right hand side else if it will return false
        // > compares both operands and returns true if operand on the left hand
        // side is greater than the value on the right hand side else if it will return true
        // >= (greater than or equal to : compares both the operands and returns the true if the
        //value on the 
        // <=

        int num1 = 20;
        int num2 = 30;

        System.out.println(num1 == num2);
        System.out.println(num1 != num2);
        System.out.println(num1 < num2);
        System.out.println(num1 > num2);
        System.out.println(num1 >= num2);
        System.out.println(num1 <= num2);
    }
}
